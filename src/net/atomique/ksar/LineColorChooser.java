    private void reset_color() {
        kSarConfig.color1 = Color.BLUE;
        kSarConfig.color2 = Color.GREEN;
        kSarConfig.color3 = Color.RED;
        kSarConfig.color4 = Color.BLACK;
        kSarConfig.color5 = Color.MAGENTA;
        kSarConfig.color6 = Color.YELLOW;
        kSarConfig.color7 = Color.CYAN;
        kSarConfig.color8 = Color.GRAY;
        kSarConfig.color9 = Color.ORANGE;
        kSarConfig.color10 = Color.PINK;
        kSarConfig.color11 = Color.DARK_GRAY;
        kSarConfig.writeDefault();
        load_color();
    }
    
    private void load_color() {
        // load color from config
        boxcolor1.setBackground(kSarConfig.color1);
        boxcolor2.setBackground(kSarConfig.color2);
        boxcolor3.setBackground(kSarConfig.color3);
        boxcolor4.setBackground(kSarConfig.color4);
        boxcolor5.setBackground(kSarConfig.color5);
        boxcolor6.setBackground(kSarConfig.color6);
        boxcolor7.setBackground(kSarConfig.color7);
        boxcolor8.setBackground(kSarConfig.color8);
        boxcolor9.setBackground(kSarConfig.color9);
        boxcolor10.setBackground(kSarConfig.color10);
        boxcolor11.setBackground(kSarConfig.color11);
    }

    private void save_color() {
        // save color to config
        kSarConfig.color1 = boxcolor1.getBackground();
        kSarConfig.color2 = boxcolor2.getBackground();
        kSarConfig.color3 = boxcolor3.getBackground();
        kSarConfig.color4 = boxcolor4.getBackground();
        kSarConfig.color5 = boxcolor5.getBackground();
        kSarConfig.color6 = boxcolor6.getBackground();
        kSarConfig.color7 = boxcolor7.getBackground();
        kSarConfig.color8 = boxcolor8.getBackground();
        kSarConfig.color9 = boxcolor9.getBackground();
        kSarConfig.color10 = boxcolor10.getBackground();
        kSarConfig.color11 = boxcolor11.getBackground();
        kSarConfig.writeDefault();
    }

