    private void colorchanger(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_colorchanger
        if ( evt == null ) {
            return;
        }
        if (evt.getSource() instanceof JTextField) {
            JTextField tmp = (JTextField) evt.getSource();
            Color tmpcolor = JColorChooser.showDialog(this, "Choose Background Color", tmp.getBackground());
            if (tmpcolor != null) {
                tmp.setBackground(tmpcolor);
            }
        }        
    }//GEN-LAST:event_colorchanger

